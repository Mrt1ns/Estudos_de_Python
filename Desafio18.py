from math import sin,cos,tan

#Angulo digitado
ang= int(input('Digite um angulo: '))

#Calculo de Seno
sn = print('O Seno de {} e {}'.format(ang,sin(ang)))

#Calculo de Cosseno
cs = print('O Cosseno do {} e {} '.format(ang,cos(ang)))

#Calculo de Tangente
tng = print('A Tangente de {} e {}'.format(ang, tan(ang)))